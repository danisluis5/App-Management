    private void setForm() {
        int row=tbMain.getSelectedRow();
        int id=(Integer)controller.getValueAt(row, 0);
        String tenTinTuc=controller.getValueAt(row, 1).toString();
        // String danhMuc=controller.getValueAt(row, 2).toString();
        String moTa =controller.getValueAt(row, 3).toString();
        String ngayDang=mNew.getItem(id).getNgayDang();
        if(!mNew.getItem(id).getChiTiet().isEmpty()){
            chiTiet = mNew.getItem(id).getChiTiet();
        }
        /*------- Convert date sql to date time and view in user ----------*/
        String xdate[] = ngayDang.split("-");        
        Date date = new Date(Integer.parseInt(xdate[0])-1900, Integer.parseInt(xdate[1])-1, Integer.parseInt(xdate[2]));
        tfID.setText(id+"");
        tfTenTin.setText(tenTinTuc+"");
        tfMoTa.setText(moTa+"");
        tfNgayDang.setValue(date);
        tfChiTiet.setText(chiTiet);
    }

    private void resetForm() {
        tfID.setText(""); 
        tfTenTin.setText("");
        tfMoTa.setText("");
        tfNgayDang.setValue(new Date());
        cbDanhMuc.setSelectedIndex(0);
        Image imagex =new ImageIcon("files/logo.apple.png").getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH);
        lbAvatar.setIcon(new ImageIcon(imagex));
        tfChiTiet.setText("");
    }
