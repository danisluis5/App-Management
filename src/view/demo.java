    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(tfTenTin.getText().isEmpty()){
            JOptionPane.showMessageDialog(new demo(), "Nhập vào tên tin tức","Thông báo",JOptionPane.WARNING_MESSAGE);
            return ;
        }
        String tenTinTuc = tfTenTin.getText();
        String moTa = tfMoTa.getText();
        String tenDanhMuc = new DanhMucComboboxModel().getElementAt(cbDanhMuc.getSelectedIndex()).getTenDanhMuc();
        String hinhAnh = imageAvatar;
        String chiTietTin = tfChiTiet.getText();
        if(controller.addItem(new TinTuc(tenTinTuc,tenDanhMuc,hinhAnh,moTa),chiTietTin)>0){
            JOptionPane.showMessageDialog(rootPane,"Thêm thành công","",JOptionPane.INFORMATION_MESSAGE);
            resetForm();
        }else{
            JOptionPane.showMessageDialog(rootPane,"Thêm thất bại","",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void UploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UploadActionPerformed
        JFileChooser chooser = new JFileChooser("C:\\Users\\vuongluis\\Pictures\\");
        int result = chooser.showDialog(null, "Upload Ảnh");
        if(result == JFileChooser.APPROVE_OPTION){
            try {
                File file = chooser.getSelectedFile();
                // lấy tên hình ảnh
                    String fileName = file.getName();
                    imageAvatar = fileName;
                    // validate or filter file extension
                    // file.getAbsoluteFile();
                    Path source = Paths.get( file.getAbsolutePath());
                    Path target = Paths.get("files/"+fileName);
                    /** If the picture is exist so this picture replace that picture. **/
                    Files.copy(source,target ,StandardCopyOption.REPLACE_EXISTING );
                    Image image =new ImageIcon("files/"+fileName).getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH);
                    lbAvatar.setIcon(new ImageIcon(image));
            } catch (IOException ex) {
                Logger.getLogger(demo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_UploadActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tbMain.getSelectedRow();
        if(row > 0){
            int id = Integer.parseInt(tfID.getText());
            if(controller.delItem(id,row)>0){
                JOptionPane.showMessageDialog(rootPane,"Xóa thành công","",JOptionPane.INFORMATION_MESSAGE);
                resetForm();
            }else{
                JOptionPane.showMessageDialog(rootPane,"Xóa thất bại","",JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane,"Chọn hàng để xóa","",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

