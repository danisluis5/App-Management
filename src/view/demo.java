    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tbMain.getSelectedRow();
        if(row >= 0){
            String tenTinTuc = tfTenTin.getText();
            String moTa = tfMoTa.getText();
            String tenDanhMuc = new DanhMucComboboxModel().getElementAt(cbDanhMuc.getSelectedIndex()).getTenDanhMuc();
            String hinhAnh = imageAvatar;
            String chiTietTin = tfChiTiet.getText();
            if(controller.editItem(new TinTuc(tenTinTuc,tenDanhMuc,hinhAnh,moTa),chiTietTin,row)>0){
                JOptionPane.showMessageDialog(rootPane,"Sữa thành công","",JOptionPane.INFORMATION_MESSAGE);
                resetForm();
            }else{
                JOptionPane.showMessageDialog(rootPane,"Sữa thất bại","",JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane,"Chọn hàng để sữa","",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

